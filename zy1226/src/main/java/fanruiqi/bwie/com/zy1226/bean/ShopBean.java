package fanruiqi.bwie.com.zy1226.bean;

public class ShopBean {
    private String title;
    private String price;

    public ShopBean(String title, String price) {
        this.title = title;
        this.price = price;
    }

    public String getTitle() {
        return title;
    }

    public String getPrice() {
        return price;
    }
}
